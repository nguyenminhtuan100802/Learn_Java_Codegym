    public static String inputAndCheckEmail() {
        Pattern pattern = Pattern.compile("^[a-zA-Z][a-zA-Z0-9]+@gmail.com$");
        Pattern pattern1 = Pattern.compile("^[0-9]+@student.hcmute.edu.vn$");
        boolean isExit = false;
        String inputUserEmail = null;
        while (!isExit) {
            System.out.print("Nhập địa chỉ email cửa bạn (vd: abc@gmail.com):");
            inputUserEmail = new Scanner(System.in).nextLine();
            if (pattern.matcher(inputUserEmail).find() || pattern1.matcher(inputUserEmail).find()) {
                isExit = true;
            }
            else {
                System.out.println(TextColor.RED + "<!> Địa chỉ email không hợp lệ, vui lòng nhập lại!" + TextColor.END_COLOR);
            }
        }
        return inputUserEmail;
    }
